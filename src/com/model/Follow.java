package com.model;

public class Follow {
	
	private int id;
	private int userid;
	private int fansid;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getUserid() {
		return userid;
	}
	public void setUserid(int userid) {
		this.userid = userid;
	}
	public int getFansid() {
		return fansid;
	}
	public void setFansid(int fansid) {
		this.fansid = fansid;
	}
	

}
